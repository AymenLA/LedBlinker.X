void APP_ButtonsStatus(void)
{
    if(true == BUTTON_IsPressed(BUTTON_CUSTOM))
    {
            LED_On(LED_CUSTOM);
    }
    else 
    {
            LED_Off(LED_CUSTOM);
    }
}

